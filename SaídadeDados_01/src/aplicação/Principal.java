/*
 * Saída de dados em Java.
 */
package aplicação;

// Classe principal.
public class Principal {

    // Método main, é a primeira instrução a ser executada, onde o programa começa.    
    public static void main(String[] args) {

        System.out.println("Ola mundo");  // Imprimir na tela a frase entre " ".
        System.out.println("16/09/2020"); // Método ou instrução.

        String login = "Aula 1"; // Definir uma variável com do tipo String.

        int numero = 100;
        System.out.println(numero); // Imprime a variável numérica.

        /* Tipos de dados */
        int A = 10;
        String B = "bloco";
        double C = 3.14;
        boolean D = false; // Variáveis do tipo boolean recebem apenas true(verdadeiro) ou false(falso). 

        System.out.println(B);

        int F = 50; // Variável do tipo inteiro declarada.
        F = 50; // Atribuir um valor a variável;
        System.out.println(F + 150); // Manipulando a variável.

    }
}
